package calculate

//AddInteger two integer
func AddInteger(x int, y int) int {
	return x + y
}

//AddFloat two float64
func AddFloat(x float64, y float64) float64 {
	return x + y
}

//AddIntegerAndFloat Sum Integer and float64
func AddIntegerAndFloat(x int, y float64) float64 {
	return float64(x) + y
}
